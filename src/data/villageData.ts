export interface Village {
  id: number
  name: string
  cover: string
  images: string[]
  intro: string
}

const villageAsset = (folderName: string, fileName: string) =>
  encodeURI(`${import.meta.env.BASE_URL}images/village/${folderName}/${fileName}`)

const buildGallery = (folderName: string, count: number) => [
  villageAsset(folderName, '封面.jpg'),
  ...Array.from({ length: count }, (_, index) => villageAsset(folderName, `${index + 1}.jpg`))
]

const buildVillage = (id: number, folderName: string, count: number, intro: string): Village => {
  const images = buildGallery(folderName, count)

  return {
    id,
    name: folderName,
    cover: images[0],
    images,
    intro
  }
}

